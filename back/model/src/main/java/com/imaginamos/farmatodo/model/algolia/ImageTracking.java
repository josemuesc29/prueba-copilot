package com.imaginamos.farmatodo.model.algolia;

public class ImageTracking {

    private String urlImageDefault;

    private String urlImageMan;

    private String urlImageWoman;

    public String getUrlImageDefault() {
        return urlImageDefault;
    }

    public void setUrlImageDefault(String urlImageDefault) {
        this.urlImageDefault = urlImageDefault;
    }

    public String getUrlImageMan() {
        return urlImageMan;
    }

    public void setUrlImageMan(String urlImageMan) {
        this.urlImageMan = urlImageMan;
    }

    public String getUrlImageWoman() {
        return urlImageWoman;
    }

    public void setUrlImageWoman(String urlImageWoman) {
        this.urlImageWoman = urlImageWoman;
    }
}
