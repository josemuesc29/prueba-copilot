package com.imaginamos.farmatodo.model.braze;

public class ItemsData {
    private String name;
    private String time;
    private BrazeProperties properties;



    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getTime() {
        return time;
    }

    public void setTime(String time) {
        this.time = time;
    }

    public BrazeProperties getProperties() {
        return properties;
    }

    public void setProperties(BrazeProperties properties) {
        this.properties = properties;
    }
}
